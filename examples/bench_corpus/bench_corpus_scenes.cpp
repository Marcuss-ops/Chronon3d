// ── B02 — Typewriter200Glyphs ──────────────────────────────────────────
Composition bench_b02_typewriter_200_glyphs() {
    return composition({
        .name = "BenchB02_Typewriter200Glyphs",
        .width = 1920, .height = 1080,
        .frame_rate = {30, 1},
        .duration = 90,
    }, [](const FrameContext& ctx) -> Scene {
        SceneBuilder s(ctx);
        if (ctx.runtime && ctx.runtime->font_engine()) s.font_engine(ctx.runtime->font_engine());

        add_dark_canvas_background(s);

        // 200-character string for the typewriter exercise (deterministic)
        // — generated by repeating a Latin pangram pattern.
        std::string typewriter_text;
        static constexpr char kPangram[] =
            "Chronon 3D is a deterministic CPU-only video renderer. "
            "Quick brown fox jumps over the lazy dog. Pack my box with "
            "five dozen liquor jugs. The five boxing wizards jump quickly.";
        for (int i = 0; i < 4; ++i) {
            typewriter_text += kPangram;
        }
        // Trim to exactly 200 glyphs (round number for indexing reports).
        if (typewriter_text.size() > 200) typewriter_text.resize(200);

        TextAnimator ta;
        ta.text(typewriter_text)
          .font_size(48.0f)
          .font_path(default_font())
          .font_weight(700)
          .color({1.0f, 1.0f, 1.0f, 1.0f})
          .align(TextAlign::Left)
          .config({
              .mode = TextAnimMode::ByCharacter,
              .duration = Frame{20},
              .delay_per_unit = Frame{2},
              .easing = EasingCurve{Easing::OutCubic},
              .animate_opacity = true,
              .animate_slide = false,
              .slide_from = {0.0f, 0.0f, 0.0f},
              .animate_scale = false,
              .scale_from = {1.0f, 1.0f, 1.0f},
          });
        ta.build(s, "typewriter");

        return s.build();
    });
}

