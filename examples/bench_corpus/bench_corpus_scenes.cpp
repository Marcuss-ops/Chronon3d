// ── B04 — Layers100 ────────────────────────────────────────────────────
Composition bench_b04_layers_100() {
    return composition({
        .name = "BenchB04_Layers100",
        .width = 1920, .height = 1080,
        .frame_rate = {30, 1},
        .duration = 90,
    }, [](const FrameContext& ctx) -> Scene {
        SceneBuilder s(ctx);
        s.screen_layer("bg", [](LayerBuilder& l) {
            l.fill(Color{0.05f, 0.05f, 0.08f, 1.0f});
        });

        // 100 independent layers; each is a rounded_rect with its own
        // position grid + opacity stagger + scale overshoot.
        for (int i = 0; i < 100; ++i) {
            const int row = i / 10;
            const int col = i % 10;
            const f32 x = (static_cast<f32>(col) - 4.5f) * 180.0f;
            const f32 y = (static_cast<f32>(row) - 4.5f) * 100.0f;
            const Frame delay = Frame{static_cast<Frame>(i % 60)};

            s.layer("L" + std::to_string(i), [&, i, x, y, delay](LayerBuilder& l) {
                l.position({x, y, 0.0f});
                l.rounded_rect("rect", {
                    .size = {120.0f, 60.0f},
                    .radius = 8.0f,
                    .color = {0.20f + (i % 10) * 0.05f,
                              0.40f + (i % 7)  * 0.04f,
                              0.80f, 0.85f},
                    .fill = std::nullopt,
                });
                auto& op = l.opacity_anim();
                op.key(Frame{0}, 0.0f, EasingCurve{Easing::Hold});
                op.key(delay,    0.0f, EasingCurve{Easing::OutCubic});
                op.key(delay + Frame{15}, 1.0f, EasingCurve{Easing::Linear});
                l.settle(0.06f, Frame{10});
            });
        }
        return s.build();
    });
}

